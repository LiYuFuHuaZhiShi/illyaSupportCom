// 登录
export function authAdminLogin(email, password) {
    return request({
        url: '/auth/sign-in/admin',
        method: 'post',
        data: {
            email: email,
            password: password
        }
    })
}

export function authAdminLogout(email, password) {
    return request({
        url: '/auth/logout/admin',
        method: 'post',
        data: {
            email: email,
            password: password
        }
    })
}

